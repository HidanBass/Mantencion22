    private void jb_ingresar3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_ingresar3ActionPerformed
        // TODO add your handling code here:
       
        ResultSet rs=null;
        String cajero=jtf_cajero.getText();
        String clave=jtp_clave.getText();
        String sql="Select *from Usuario where nombre_cajero ='"+cajero+"';";
        rs=acc.ejecutarQuery(sql);
        try {
            if (rs.next())
            {
                String pass=rs.getString(3); 
                if (pass.equalsIgnoreCase(clave))
                {
                    JOptionPane.showMessageDialog(null,"Bienvenido");
                    JFRAME_inicio.setVisible(false);  
                    Framecajero.setVisible(false);
                    FrameVentas.setVisible(true);
                    FrameVentas.pack();
                    FrameVentas.setLocationRelativeTo(this);
                          
                }
                else
                {
                    JOptionPane.showMessageDialog(null," Clave Incorrecta");
                    
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Usuario NO Existe");
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(PrincipalContenedor.class.getName()).log(Level.SEVERE, null, ex);
        }
       
        
        
    }//GEN-LAST:event_jb_ingresar3ActionPerformed

