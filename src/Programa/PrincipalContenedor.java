        // TODO add your handling code here:
        ResultSet rs=null;
        String rutsocio= jtf_rut2.getText();
        String ru;
        String no;
        String ap;
        String co;
        String te;
        String sql="Select *from Socio where rut ='"+rutsocio+"';";
        rs=acc.ejecutarQuery(sql);
        try {
            if (rs.next())

                {
                    JOptionPane.showMessageDialog(null,"Socio Encontrado");

                    ru=rs.getString(1);
                    jtf_rut2.setText(ru);
                    jtf_rut2.enable(false);
                    no=rs.getString(2);
                    jtf_nombres2.setText(no);
                    ap=rs.getString(3);
                    jtf_apellidos2.setText(ap);
                    co=rs.getString(4);
                    jtf_correo2.setText(co);
                    te=rs.getString(5);
                    jtf_telefono2.setText(te);
                }
            else{
                JOptionPane.showMessageDialog(null,"El Rut No Se Encuentra En La Base De Datos");
                jtf_codigoBarra.setText("");
            }
              
           
        } catch (SQLException ex) {
            Logger.getLogger(PrincipalContenedor.class.getName()).log(Level.SEVERE, null, ex);
        }
